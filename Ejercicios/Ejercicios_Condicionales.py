#1. Ejercicio: Dado un número, imprime si es positivo o negativo.

def par_impar(num):
  if num < 0:
    print(f'El número {num} es Negativo')

  elif num == 0:
    print(f'El número {num} es Cero')
  elif num > 0:
    print(f'El número {num} es Positivo')

par_impar(5)

